$bool = 10 != 10;
var_dump($bool);

$bool = 10 !== 10;
var_dump($bool);

$bool = 10 > 10;
var_dump($bool);

$bool = 10 >= 10;
var_dump($bool);

$bool = 10 < 10;
var_dump($bool);

$bool = 10 <= 10;
var_dump($bool);
